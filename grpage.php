ABSTRACT</h3>

<h4>
"Fibred Contextual Quantum Physics"</h4>
<p>
PhD Thesis, School of Computer Science, University of Birmingham, 2014. 137 pages.</p>
<p>
Summary</p>
<p>
Inspired by the recast of the quantum mechanics in a toposical framework, we develop
a contextual quantum mechanics using only the geometric mathematics to propose a quantum
contextuality adaptable in every topos.
The contextuality adopted here corresponds to the
belief that the quantum world must only be seen from the classical viewpoints &agrave; la Bohr and
consequently putting forth the notion of a context, while retaining a realist understanding.
Mathematically, the cardinal object is a spectral Stone bundle
$\Sigma \rightarrow \mathcal{B}$ (between stablycompact locales)
permitting a treatment of the kinematics,
fibre by fibre and fully point-free.
In leading naturally to a new notion of point, the geometricity permits to understand those
of the base space $\mathcal{B}$ as the contexts $C$
- the commutative C*-algebras of a incommutative C*-algebra -
and those of the spectral locale $\Sigma$ as the couples $(C,\psi)$,
with $\psi$ a state of the system from the perspective of such a $C$.
The contexts are furnished with a natural order,
the aggregation order which is installed as the specialization on $\mathcal{B}$ and $\Sigma$
thanks to (one part of) the Priestley's duality adapted geometrically
as well as to the effectuality of the lax descent of the Stone bundles along the perfect maps.</p>



